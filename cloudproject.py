#11. SCENARIO ANALYSIS
# defining scenario projection function
# arguments: model: trained lstm model
# data_scaled: scaled dataset after minmaxscalar
# n_years : number of future years to predict
# time_steps: number of past years the model uses for prediction
# scenario : defines the baseline, high and low emissions

def scenario_projection(model,data_scaled,n_years,time_steps,scenario="baseline"):
    # takes the last time_steps rows from the scaled dataset
    # this becomes the starting input for future prediction
    # .copy() : prevents modifying original data
    input_seq=data_scaled[-time_steps:].copy()
    # future_predictions : creating an empty list which stores all predicted future values
    future_predictions=[]


    # dictionary that defines
    # 1. Baseline: 0 changes
    # 2. High : increases the prediction by 1%
    # 3. Low : decreases the prediction by 1%
    scenario_factor = {
        "baseline":0.0,
        "high":0.01,
        "low":-0.01
    }
    # gets the factor based on the selected scenario. If the scenario not found default is 0.0
    factor=scenario_factor.get(scenario,0.0)

    # each loop predicts one future year
    for _ in range(n_years):
        # reshaping input to match lstm format: (samples,time_steps,features)
        # predicts next year's values
        pred=model.predict(input_seq.reshape(1,time_steps,data_scaled.shape[1]),verbose=0)
        # applying the scenario adjustments like high and low emissions
        adjusted_pred=pred+factor*pred
        # adds predicted values to the list
        # [0] removes extra dimension
        future_predictions.append(adjusted_pred[0])
        # updating input sequence
        # removes the oldest year input_seq[1:]
        # adds new predicted year at bottom this creates a rolling window
        # so, the prediction keeps moving forward year by year
        input_seq=np.vstack((input_seq[1:],adjusted_pred))
    # converts the list to numpy array. Returns the predicted future values
    return np.array(future_predictions)

