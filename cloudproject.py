# project future: function to predict future yrs

def project_future(model,data_scaled,n_years,time_steps):

    # Future predictions: an empty list is used to store predictions
    future_predictions=[]
    # takes last 8 yrs as input
    input_seq=data_scaled[-time_steps:].copy()
    # repeat the prediction for number of future years
    for _ in range(n_years):
        # input is reshaped to (1,sample,8 years,features)
        pred=model.predict(input_seq.reshape(1,time_steps,data_scaled.shape[1]),verbose=0)
        future_predictions.append(pred[0])
        # Removes oldest year and adds predicted year so, that prediction becomes input for next prediction
        # This is called recursive multi step forecasting
        input_seq=np.vstack((input_seq[1:],pred))
    # returns the predicted values
    return np.array(future_predictions)

