# Scenario projection function
def scenario_projection(model, data_scaled, n_years, time_steps, scenario="baseline"):

    input_seq = data_scaled[-time_steps:].copy()
    future_predictions = []

    scenario_factor = {
        "baseline": 0.0,
        "high": 0.01,
        "low": -0.01
    }

    factor = scenario_factor.get(scenario, 0.0)

    for _ in range(n_years):

        pred = model.predict(
            input_seq.reshape(
                1,
                time_steps,
                data_scaled.shape[1]
            ),
            verbose=0
        )

        adjusted_pred = pred + factor * pred

        future_predictions.append(adjusted_pred[0])

        input_seq = np.vstack((input_seq[1:], adjusted_pred))

    return np.array(future_predictions)


# Predict 20 years
