time_steps = 8

# Function to predict future years
def project_future(model, data_scaled, n_years, time_steps):

    future_predictions = []

    # Last 8 years
    input_seq = data_scaled[-time_steps:].copy()

    for _ in range(n_years):

        pred = model.predict(
            input_seq.reshape(
                1,
                time_steps,
                data_scaled.shape[1]
            ),
            verbose=0
        )

        future_predictions.append(pred[0])

        input_seq = np.vstack((input_seq[1:], pred))

    return np.array(future_predictions)

# Predict next 20 years
